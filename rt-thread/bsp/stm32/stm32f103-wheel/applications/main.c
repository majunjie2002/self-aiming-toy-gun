static void uart5_rx_entry(void* parameter)
{
	rt_err_t result = RT_EOK;
	rt_uint8_t *pbuf = RT_NULL;
    rt_size_t szbuf=0;
	
	pbuf = rt_malloc(BUFFER_SIZE);
    RT_ASSERT(pbuf != RT_NULL);
	
	rt_thread_mdelay(1000);
	rt_device_write(dev2, 0, set_motor_modbus1, MOTOR_SIZE);
	rt_thread_mdelay(1000);
	rt_device_write(dev2, 0, set_motor_modbus2, MOTOR_SIZE);
	rt_thread_mdelay(1000);
	rt_device_write(dev2, 0, set_motor_acc1, MOTOR_SIZE);
	rt_thread_mdelay(1000);
	rt_device_write(dev2, 0, set_motor_acc2, MOTOR_SIZE);
	rt_thread_mdelay(1000);
	
    while (RT_TRUE)
	{
        result = rt_sem_take(semaph5, RT_WAITING_FOREVER);
		if(result == -RT_ETIMEOUT)
            continue; 
        
        switch (szbuf){
        case 0:
        case 1:
            szbuf += rt_device_read(dev5, 0, pbuf + szbuf, 1);
            if (pbuf[0] != 0xEE)
                szbuf = 0;
            break;
        default:
            szbuf += rt_device_read(dev5, 0, pbuf + szbuf, CMD_SIZE - szbuf);
            break;
        }
        if (szbuf != CMD_SIZE)
            continue;   
        szbuf = 0;

		if(pbuf[1]==0)
		{
			motor_speed1(pbuf[2]);
		}
		else
		{
			motor_speed1(-pbuf[2]);
		}
		rt_thread_mdelay(10);
		if(pbuf[3]==0)
		{
			motor_speed2(pbuf[4]);
		}
		else
		{
			motor_speed2(-pbuf[4]);
		}
		if(pbuf[5]==0)
		{
			rt_pin_write(RELAY_PIN0, PIN_LOW);
		}
		else
		{
			rt_pin_write(RELAY_PIN0, PIN_HIGH);
		}
		if(pbuf[6]==0)
		{
			rt_pin_write(RELAY_PIN1, PIN_LOW);
		}
		else
		{
			rt_pin_write(RELAY_PIN1, PIN_HIGH);
		}
    }
}

int main(void)
{
	rt_err_t result = RT_EOK;
	rt_thread_t pthread = RT_NULL;
	
    rt_pin_mode(LED_PIN, PIN_MODE_OUTPUT);
    rt_pin_mode(MODE_PIN, PIN_MODE_OUTPUT);
	rt_pin_mode(RELAY_PIN0, PIN_MODE_OUTPUT);
    rt_pin_mode(RELAY_PIN1, PIN_MODE_OUTPUT);
	rt_pin_write(RELAY_PIN0, PIN_LOW);
	rt_pin_write(RELAY_PIN1, PIN_LOW);
	
	dev2 = rt_device_find("uart2");
    RT_ASSERT(dev2 != RT_NULL);
    rt_device_open(dev2, RT_DEVICE_OFLAG_RDWR | RT_DEVICE_FLAG_INT_RX);
	struct serial_configure config2 = RT_SERIAL_CONFIG_DEFAULT;
    config2.baud_rate = BAUD_RATE_19200;
    rt_device_control(dev2, RT_DEVICE_CTRL_CONFIG, &config2);
	
	dev5 = rt_device_find("uart5");
    RT_ASSERT(dev5 != RT_NULL);
    rt_device_open(dev5, RT_DEVICE_OFLAG_RDWR | RT_DEVICE_FLAG_INT_RX);
	semaph5 = rt_sem_create("sh5" ,0, RT_IPC_FLAG_FIFO);
    RT_ASSERT(semaph5 != RT_NULL);
	rt_device_set_rx_indicate(dev5, uart5_hook_callback);
	pthread = rt_thread_create("rx5", uart5_rx_entry, RT_NULL, 2048, 6, 20);
    RT_ASSERT(pthread != RT_NULL);
    result = rt_thread_startup(pthread);
    RT_ASSERT(result == RT_EOK);
    
    while(RT_TRUE)
    {
        rt_thread_mdelay(500);
        
        if (rt_pin_read(LED_PIN))
		{
            rt_pin_write(LED_PIN, PIN_LOW);
			rt_pin_write(MODE_PIN, PIN_LOW);
		}
        else
		{
            rt_pin_write(LED_PIN, PIN_HIGH);
			rt_pin_write(MODE_PIN, PIN_HIGH);
		}
    }
    
    // never be here.
}
